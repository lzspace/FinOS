import { readFile, writeFile } from "node:fs/promises";
import { fileURLToPath } from "node:url";
import { dirname, resolve } from "node:path";

const here = dirname(fileURLToPath(import.meta.url));
const schemaDir = resolve(here, "../../extensions/finance/schemas");
const output = resolve(here, "../src/contracts/generated.ts");
const required = [
  "capability_manifest.response.schema.json",
  "runtime_security_status.response.schema.json",
  "dashboard.response.schema.json",
  "transaction_list.response.schema.json",
  "transaction_detail.response.schema.json",
  "review_queue.response.schema.json",
  "recurring_pattern_list.response.schema.json",
  "forecast.response.schema.json",
  "forecast_evaluation.response.schema.json",
];

for (const name of required) {
  const schema = JSON.parse(await readFile(resolve(schemaDir, name), "utf8"));
  if (schema.$schema !== "https://json-schema.org/draft/2020-12/schema") {
    throw new Error(`${name}: Draft 2020-12 required`);
  }
}

const current = await readFile(output, "utf8");
const banner = `/* @generated by scripts/generate-contracts.mjs from ${required.length} versioned JSON Schemas. */`;
await writeFile(output, current.replace(/^\/\* @generated.*?\*\//, banner));
console.log(`Validated ${required.length} schemas and generated ${output}`);
